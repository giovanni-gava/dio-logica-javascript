// For é utilizado para repetir um bloco de código várias vezes

for (let contador = 0; contador < 6; contador++) { 
    console.log(contador);
    console.log("Aumentando o contador")
}


// O for é composto por 3 partes: 
// a declaração da variável, a condição de parada e o incremento
// Esse for está declarando a variável contador com valor 0
// A condição de parada é que o contador seja menor que 4
// E o incremento é que o contador seja incrementado em 1 a cada iteração