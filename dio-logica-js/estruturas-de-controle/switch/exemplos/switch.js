// Estrutura de controle de fluxo Switch
// Switch é uma estrutura de controle de fluxo que permite que o programa tome decisões com base no valor de uma expressão.


let fruit = "laranja";

switch(fruit) {
    case "laranja":
    console.log("Suco de laranja");
    break 

    case "banana":
    console.log("Vitamina de banana");
    break;
    
    case "mamao":
    console.log("Vitamina de mamão");
} 